class pacman {
public:
    int x{TS};
    int y{TS};
};

class ghost {
public:
    unsigned char r, g, b;
    int x, y;
};

void draw_pellet(SDL_Renderer *renderer, int x, int y) {
    SDL_Rect rect{x, y, 4, 4};
    SDL_SetRenderDrawColor(renderer, 0xFF, 0xFF, 0xFF, 0xFF);
    SDL_RenderFillRect(renderer, &rect);
}

void draw_pacman(SDL_Renderer *renderer, int x, int y) {
    SDL_Rect rect{x, y, TS, TS};
    SDL_SetRenderDrawColor(renderer, 0xFF, 0xFF, 0x00, 0xFF);
    SDL_RenderFillRect(renderer, &rect);
}

void draw_ghost(SDL_Renderer *renderer, ghost *g) {
    SDL_Rect rect{g->x, g->y, TS, TS};
    SDL_SetRenderDrawColor(renderer, g->r, g->g, g->b, 0xFF);
    SDL_RenderFillRect(renderer, &rect);
}


int main(int argc, char *argv[]) {
    SDL_Window *window;
    SDL_Renderer *renderer;

    if (SDL_Init(SDL_INIT_VIDEO) < 0) {
        SDL_LogError(SDL_LOG_CATEGORY_APPLICATION, "Couldn't initialize SDL: %s", SDL_GetError());
        return 3;
    }

    if (SDL_CreateWindowAndRenderer(MAP_WIDTH * TS, MAP_HEIGHT * TS, SDL_WINDOW_RESIZABLE, &window, &renderer)) {
        SDL_LogError(SDL_LOG_CATEGORY_APPLICATION, "Couldn't create window and renderer: %s", SDL_GetError());
        return 3;
    }

    // load map
    // load pacman
    // load ghosts
    pacman pac{128, 128};

    ghost blinky{255, 0, 0, TS, TS};
    ghost pinky{255, 184, 255, TS * 2, TS};
    ghost inky{0, 255, 255, TS, TS * 2};
    ghost clyde{255, 184, 82, TS * 2, TS * 2};

    SDL_Event event;
    while (1) {
        SDL_PollEvent(&event);
        if (event.type == SDL_QUIT) {
            break;
        }
        if (event.type == SDL_KEYDOWN) {
            if (event.key.keysym.sym == SDLK_j) {}
            if (event.key.keysym.sym == SDLK_k) {}
            if (event.key.keysym.sym == SDLK_l) {}
            if (event.key.keysym.sym == SDLK_i) {}
        }

        SDL_SetRenderDrawColor(renderer, 0x00, 0x00, 0x00, 0x00);
        SDL_RenderClear(renderer);

        // render ghosts
        draw_ghost(renderer, &blinky);
        draw_ghost(renderer, &pinky);
        draw_ghost(renderer, &inky);
        draw_ghost(renderer, &clyde);

        // render map
        for (int i = 0; i < MAP_HEIGHT; i++) {
            for (int j = 0; j < MAP_WIDTH; j++) {
                if (map[i][j] == 1) {
                    SDL_Rect rect{(j * TS) + (TS / 4), (i * TS) + TS / 4, TS / 3, TS / 3};
                    SDL_SetRenderDrawColor(renderer, 0x00, 0x00, 0xFF, 0xFF);
                    SDL_RenderFillRect(renderer, &rect);
                }
            }
        }

        // render ghosts
        // render pacman
        // render map
        // render pellets
        // render ui

        SDL_RenderPresent(renderer);
    }

    SDL_DestroyRenderer(renderer);
    SDL_DestroyWindow(window);

    SDL_Quit();

    return 0;
}
